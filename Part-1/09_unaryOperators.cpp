// increament unary operators:

#include <iostream>
using namespace std;

int main(){
    int a = 10;

    int b = a++;

    cout << "b = " << b << endl;
    cout <<"a = " << a << endl;


     //alternate condition

    int c = 12;
    int d = ++c;

    cout << "c = " << c << endl;
    cout << "d = " << d << endl;

    return 0 ;
}

// decreament unary operators:

